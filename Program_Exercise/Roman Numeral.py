# This program asks a user for an input between number 1 and 10 
# to get a Roman numeral as the corresponding output.
  
number = int(input('Enter a number between 1 and 10: '))

if number == 1:
    print('I')
elif number == 2:
    print('II')
elif number == 3:
    print('III')
elif number == 4:
    print('IV')
elif number == 5:
    print('V')
elif number == 6:
    print('VI')
elif number == 7:
    print('VII')
elif number == 8:
    print('VIII')
elif number == 9:
    print('IV')
elif number == 10:
    print('X')
else:
    print("You didn't enter a number between 1 and 10!")
